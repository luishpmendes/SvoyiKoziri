package svoyikoziri.engine;

/**
 * A enumeração <code>PlayType</code> representa os tipos de jogadas do jogo 
 * de cartas Svoyi Koziri.
 *
 * @author Luis H. P. Mendes
 */
public enum PlayType {
    /**
     * O tipo de jogada em que se joga uma carta.
     */
    PLAYACARD,
    /**
     * O tipo de jogada em que se pega todas as cartas da pilha de cartas 
     * da mesa.
     */
    TAKEALLCARDS
}
